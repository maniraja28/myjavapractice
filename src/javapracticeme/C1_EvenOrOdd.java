package javapracticeme;

public class C1_EvenOrOdd {
	//if any number is divisible with 2 the reminder should be zero it should be even number or otherwise odd number
	public static void main(String[] args) {
		int i=19;
		if(i%2==0) {
			System.out.println(i + " is even number");
		}
		else {
			System.out.println(i + " is odd number");
		}
		
	}

}
